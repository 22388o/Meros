#Keccak Tests.

#Hash lib.
import ../../../src/lib/Hash

#"" vector on 256.
assert(
    Keccak_256("") == "C5D2460186F7233C927E7DB2DCC703C0E500B653CA82273B7BFAD8045D85A470"
)
#"abc" vector on 256.
assert(
    Keccak_256("abc") == "4E03657AEA45A94FC7D47BA826C8D667C0D1E6E33A64A036EC44F58FA12D6C45"
)

#"" vector on 512.
assert(
    Keccak_512("") == "0EAB42DE4C3CEB9235FC91ACFFE746B29C29A8C366B7C60E4E67C466F36A4304C00FA9CAF9D87976BA469BCBE06713B435F091EF2769FB160CDAB33D3670680E"
)
#"abc" vector on 512.
assert(
    Keccak_512("abc") == "18587DC2EA106B9A1563E32B3312421CA164C7F1F07BC922A9C83D77CEA3A1E5D0C69910739025372DC14AC9642629379540C17E2A65B19D77AA511A9D00BB96"
)

echo "Finished the lib/Hash/Keccak test."
